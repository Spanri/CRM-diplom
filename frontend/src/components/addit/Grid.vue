<template>
	<div class="grid">
		<form id="search">
			Поиск по всем столбцам <input name="query" v-model="filterKey">
		</form>
        <table>
            <thead>
                <tr>
                    <th v-for="(key,i) in columns"
                        :key="i"
                        @click="sortBy(key.title)"
                        :class="{ active: sortKey == key }">
                        {{ key.title | capitalize }}
                    <span class="arrow" :class="sortOrders[key] > 0 ? 'asc' : 'dsc'">
                    </span>
                    </th>
                </tr>
            </thead>
            <tbody>
                <tr v-for="(entry,j) in filteredHeroes" :key="j" @click="toDoc(entry.doc.id)">
                    <td v-for="(key,i) in columns" :key="i">
                    {{entry[key.key]}}
                    </td>
                </tr>
            </tbody>
        </table>
	</div>
</template>

<script>
import { mapGetters, mapState } from 'vuex'
import {DOCS_REQUEST, DOC_REQUEST, USER_REQUEST, USER_NOTIF_REQUEST, DOC_FOLDER_PAGE, DOC_FOLDER_PAGE_PROFILE} from '../../store/mutation-types'

export default {
    name: 'grid',
    props: {
        id: String,
        columns: Array,
    },
    data: function () {
        var sortOrders = {}
        this.columns.forEach(function (key) {
            sortOrders[key] = 1
        });
        return {
            h: null,
            sortKey: '',
            filterKey: '',
            sortOrders: sortOrders
        }
    },
    created(){
        this.$store.dispatch(USER_REQUEST)
        this.$store.dispatch(DOCS_REQUEST)
    },
    computed: {
        ...mapGetters({
            getProfile: 'getProfile',
            getDocs: 'getDocs',
            // token: 'token',
            // getNotif: 'getNotif',
        }),
        filteredHeroes() {
            var sortKey = ''
            var sortOrders = {}
            this.columns.forEach(function (key) {
                sortOrders[key] = 1
            });
            var filterKey = this.filterKey && this.filterKey.toLowerCase()
            var order = this.sortOrders[sortKey] || 1
            var heroes = this.heroes || this.h;
            if (filterKey) {
                heroes = heroes.filter(function (row) {
                    return Object.keys(row).some(function (key) {
                        return String(row[key]).toLowerCase().indexOf(filterKey) > -1
                    })
                })
            }
            if (sortKey) {
                heroes = heroes.slice().sort(function (a, b) {
                    a = a[sortKey]
                    b = b[sortKey]
                    return (a === b ? 0 : a > b ? 1 : -1) * order
                })
            }
            console.log('heroes', heroes)
            return heroes
        },
		heroes() {
            if(this.$route.params.id == 'all'){
                this.$store.commit(DOC_FOLDER_PAGE, 1)
			    return this.getDocs;
            } else if(this.$route.params.id == 'common') {
                this.$store.commit(DOC_FOLDER_PAGE, 2)
                return this.getDocs
                .filter(d => d.doc.common == true);
            } else if(this.$route.params.id == 'myDocs') {
                this.$store.commit(DOC_FOLDER_PAGE, 3)
                return this.getDocs
                .filter(d => d.user.id == this.getProfile.id && d.is_owner == true);
            } else if(this.$route.params.id == 'signature-request') {
                this.$store.commit(DOC_FOLDER_PAGE, 4)
                return this.getDocs
                .filter(d => d.is_owner == false && d.is_signature_request == true && d.is_signature == false);
            } else if(this.$route.params.id == 'signature-success') {
                this.$store.commit(DOC_FOLDER_PAGE, 5)
                return this.getDocs
                .filter(d => d.is_owner == false && d.is_signature_request == true && d.is_signature == true);
            } else if(this.$route.params.id == 'available-to-me') {
                this.$store.commit(DOC_FOLDER_PAGE, 6)
                return this.getDocs
                .filter(d => d.is_owner == false && d.is_signature_request == false);
            } else if(this.id == 'notif') {
                this.$store.commit(DOC_FOLDER_PAGE_PROFILE, 1)
                return this.getDocs
                .filter(d => d.is_owner == false && d.is_signature_request == true && d.is_signature == false);
                return null;
            }
		}
    },
    filters: {
        capitalize(str) {
            return str.charAt(0).toUpperCase() + str.slice(1)
        }
    },
    methods: {
        sortBy(key) {
            this.sortKey = key
            this.sortOrders[key] = this.sortOrders[key] * -1
        },
        toDoc(id){
            this.$router.push('/document/'+id);
        },
    }
}
</script>

